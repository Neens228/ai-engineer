"""
Задание 7b — input/вывод. Мини-тема (запускается ВРУЧНУЮ, без авто-проверки).

Реализуй простой калькулятор-диалог:
  1. Спроси у пользователя имя        -> input(...)
  2. Спроси два числа                 -> не забудь int(input(...)), input даёт строку!
  3. Поприветствуй по имени и выведи их сумму через f-строку.

Запуск:  python input_demo.py
Ожидаемый диалог (пример):
    Как тебя зовут? Игорь
    Первое число: 7
    Второе число: 5
    Игорь, сумма = 12
"""


def main() -> None:
    name = input("Как тебя зовут? ")
    first_number = int(input("Первое число: "))
    second_number = int(input("Первое число: "))
    print(f"{name}, сумма = {first_number + second_number}")



if __name__ == "__main__":
    main()
